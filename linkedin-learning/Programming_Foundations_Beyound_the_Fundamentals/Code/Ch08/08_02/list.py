# Using built-in classes

flips = [
  "heads",
  "tails",
  "tails",
  "heads",
  "tails"
]


# .count() method return  number of elements that match certain string.
print(flips.count("heads"))
# .pop() method return the final element in the list .
print(flips.pop())


# The list inherits methods like count and pop from class Array.