# if-else Statement 
# "else clause" => we don't use the elsewithout an if statement.


# if  plant == Cacti  => True => " Cacti don't need a lot of water "
# if plant == Cacti => False => else => " Irises love water "

# print "Thanks"

plant = "Irises"

if plant == "Cacti":
    print(plant, "don't need a lot of water")
else:
    print(plant, "love water")

